public class cgpa {
    //cgpa=Cumulative Grade Point Average.
    public static void main(String[] args) {
        System.out.println("subject cgpa");
        float sub = 45.0f;
        float sub1 = 48.0f;
        float sub2 = 98.0f;
        float cgpa = (sub + sub1 + sub2) / 30.0f;
        System.out.println(cgpa);

    }

}
