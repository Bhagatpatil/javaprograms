/*in the program use a wrapper class
 * This wrapper class type is boxing wrapper class
 * it is use for convert primitive data type to object
 * */
package adj3;

public class autoboxing_wrapper_class_01 {

    public static void main(String[] args) {

        //code of boxing
        int i = 100;// it is primitive data type this value is 100
        Integer j = i;//it is convert primitive data type to object
        System.out.println(j);//then print object
    }
}
