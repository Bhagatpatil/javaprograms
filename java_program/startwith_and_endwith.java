public class startwith_and_endwith {
    public static void main(String[] args) {
        String name="niharika";
        System.out.println(name.startsWith("ni"));
        // it is startwith argument 
        //output is=true

        System.out.println(name.endsWith("ka"));
        //it is endwith argument 
        //output is=true
        System.out.println(name.endsWith("ra"));
        //when is it not endwith "ka" then output is false
        
    }
    
}
